%
%  Modified:
%
%    31 January 2012
%
%  Author:
%
%    John Burkardt
%
%  Parameters:
%
%    Input, real T, the current time.
%
%    Input, real U, the current solution value.
%
%    Output, real VALUE, the value of the derivative, dU/dT.
%
%u*((2.7182)^(
   value=amount*-k*tempCoefficient;
  
    return
end


